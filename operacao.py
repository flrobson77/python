A = int(input("Digite um número: "))
B = int(input("Digite outro número: "))

print("\nOperações Aritméticas")

SOMA = A + B
SUBT = A - B
PROD = A * B
QUOC = A / B
REST = A % B
INTE = A // B
POTE = A ** B

print(A,"+",B,"=",SOMA)
print(A,"-",B,"=",SUBT)
print(A,"x",B,"=",PROD)
print(A,"/",B,"=",QUOC)
print(A,"%",B,"=",REST)
print(A,"//",B,"=",INTE)
print(A,"**",B,"=",POTE)